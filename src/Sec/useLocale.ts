// Map browser language to our locale codes
function detectLocale(): LocaleCode {
  const lang = navigator.language?.toLowerCase() || 'he';
  if (lang.startsWith('he')) return 'he';
  if (lang.startsWith('ar')) return 'ar';
  if (lang.startsWith('ru')) return 'ru';
  if (lang.startsWith('nl')) return 'nl';
  if (lang.startsWith('fr')) return 'fr';
  if (lang.startsWith('de')) return 'de';
  if (lang.startsWith('es')) return 'es';
  if (lang.startsWith('en')) return 'en';
  return 'en'; // fallback
}

export function useLocale() {
  const [locale, setLocale] = useState<LocaleCode>(detectLocale);

  // Text direction
  const dir = useMemo(() => (locale === 'he' || locale === 'ar') ? 'rtl' : 'ltr', [locale]);

  // Speech recognition language code
  const speechLang = useMemo(() => {
    const map: Record<LocaleCode, string> = {
      he: 'he-IL', en: 'en-US', ar: 'ar-SA', ru: 'ru-RU',
      nl: 'nl-NL', fr: 'fr-FR', de: 'de-DE', es: 'es-ES',
    };
    return map[locale];
  }, [locale]);

  const t = useCallback((key: string): string => {
    return translations[locale]?.[key] || translations.en?.[key] || key;
  }, [locale]);

  return { locale, setLocale, dir, t, speechLang };
}
